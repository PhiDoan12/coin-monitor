package com.coin;

public class TakeIncreaseAmount {
    private String symbol;
    private String price;
    private String coinBuySymbol;
    private String priceCoinBuy;

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getCoinBuySymbol() {
        return coinBuySymbol;
    }

    public void setCoinBuySymbol(String coinBuySymbol) {
        this.coinBuySymbol = coinBuySymbol;
    }

    public String getPriceCoinBuy() {
        return priceCoinBuy;
    }

    public void setPriceCoinBuy(String priceCoinBuy) {
        this.priceCoinBuy = priceCoinBuy;
    }
}
